# Решение 1
def func1_1(line):
    REGEX1 = r'([a-z]*)[A-Z]+([a-z]*)'
    found = re.findall(REGEX1, line)
    flatten_list = itertools.chain.from_iterable(found)
    filtered_list = list(filter(lambda x: len(x)>0, flatten_list))
    return filtered_list

